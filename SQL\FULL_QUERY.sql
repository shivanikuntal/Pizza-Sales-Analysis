-- QUERYS 
--1. Retrieve all orders.
SELECT * FROM ORDERS;
--2. Count the total number of orders.
SELECT COUNT(*) AS TOTAL_ORDER FROM ORDERS;
--3. Find the total number of pizzas sold.
SELECT SUM(QUANTITY) AS NO_OF_PIZZA_SOLD FROM  ORDER_DETAILS;
--Calculate the total revenue generated.
SELECT ROUND(SUM(P.PRICE*OD.QUANTITY),2) AS TOTAL_REVENUE 
FROM ORDER_DETAILS OD
JOIN PIZZAS P
ON P.PIZZA_ID = OD.PIZZA_ID;
--5. Find the average order value.
SELECT ROUND(SUM(P.PRICE*OD.QUANTITY)/COUNT(DISTINCT OD.ORDER_ID),2) AS AVG_ORDER_VALUE
FROM ORDER_DETAILS OD
JOIN PIZZAS P
ON P.PIZZA_ID = OD.PIZZA_ID;
--6. Highest Priced Pizza.
SELECT P.P_SIZE ,P.PRICE , PT.NAME FROM PIZZAS P
JOIN PIZZA_TYPES PT
ON P.PIZZA_TYPE_ID = PT.PIZZA_TYPE_ID
ORDER BY P.PRICE DESC
LIMIT 1;
--7. Lowest Priced Pizza.
SELECT P.P_SIZE ,P.PRICE , PT.NAME FROM PIZZAS P
JOIN PIZZA_TYPES PT
ON P.PIZZA_TYPE_ID = PT.PIZZA_TYPE_ID
ORDER BY P.PRICE 
LIMIT 1;
--8. Number of Pizza Types.
SELECT COUNT(*) AS NO_OF_PIZZA_TYPES 
FROM PIZZA_TYPES;
--9. Pizzas in Each Category.
SELECT CATEGORY , COUNT(*) FROM PIZZA_TYPES
GROUP BY CATEGORY;
--10 Revenue by Category.
SELECT PT.CATEGORY , ROUND(SUM(P.PRICE*OD.QUANTITY),2) AS CATEGORY_REVENUE
FROM PIZZA_TYPES PT
JOIN PIZZAS P
ON PT.PIZZA_TYPE_ID = P.PIZZA_TYPE_ID
JOIN ORDER_DETAILS OD
ON P.PIZZA_ID = OD.PIZZA_ID
GROUP BY PT.CATEGORY
ORDER BY CATEGORY_REVENUE DESC;
--11. Revenue by Pizza Size.
SELECT P.P_SIZE , ROUND(SUM(P.PRICE*OD.QUANTITY),2) AS SIZE_REVENUE
FROM PIZZAS P
JOIN ORDER_DETAILS OD
ON P.PIZZA_ID = OD.PIZZA_ID 
GROUP BY P.P_SIZE
ORDER BY SIZE_REVENUE DESC;
--12. Top 10 Best Selling Pizzas.
SELECT PT.NAME , SUM(OD.QUANTITY) AS TOTAL_SOLD
FROM PIZZAS P
JOIN ORDER_DETAILS OD
ON P.PIZZA_ID = OD.PIZZA_ID
JOIN PIZZA_TYPES PT
ON P.PIZZA_TYPE_ID = PT.PIZZA_TYPE_ID
GROUP BY PT.NAME
ORDER BY TOTAL_SOLD DESC
LIMIT 10;
--13. Top 10 Revenue Generating Pizzas.
SELECT PT.NAME ,ROUND(SUM(P.PRICE*OD.QUANTITY),2)AS TOTAL_REVENUE
FROM PIZZAS P
JOIN ORDER_DETAILS OD
ON P.PIZZA_ID = OD.PIZZA_ID
JOIN PIZZA_TYPES PT
ON P.PIZZA_TYPE_ID = PT.PIZZA_TYPE_ID
GROUP BY PT.NAME
ORDER BY TOTAL_REVENUE DESC
LIMIT 10;
--14. Busiest Hour.
SELECT EXTRACT(HOUR FROM TIME) AS HOURS,
COUNT(*) AS TOTAL_ORDERS FROM ORDERS
GROUP BY HOURS
ORDER BY TOTAL_ORDERS DESC;
--15. Orders by Day.
SELECT TO_CHAR(DATE,'DAY') AS DAYS,
COUNT(*) AS TOTAL_ORDERS FROM ORDERS
GROUP BY DAYS
ORDER BY TOTAL_ORDERS DESC;
--16. Orders by Month.
SELECT TO_CHAR(DATE,'MONTH') AS MONTHS,
COUNT(*) AS TOTAL_ORDERS FROM ORDERS
GROUP BY MONTHS
ORDER BY TOTAL_ORDERS DESC;
--17. Top 5 Most Expensive Pizzas.
SELECT PT.NAME , P.P_SIZE, P.PRICE
FROM PIZZAS P
JOIN PIZZA_TYPES PT
ON P.PIZZA_TYPE_ID =PT.PIZZA_TYPE_ID
