--19. Revenue by Month.
SELECT TO_CHAR(O.DATE,'MONTH') AS MONTHS,
ROUND(SUM(P.PRICE*OD.QUANTITY),2) AS REVENUE
FROM ORDERS O
JOIN ORDER_DETAILS OD
ON OD.ORDER_ID = O.ORDER_ID
JOIN PIZZAS P
ON P.PIZZA_ID = OD.PIZZA_ID
GROUP BY MONTHS 
ORDER BY REVENUE DESC;
--20. Revenue by Day.
SELECT TO_CHAR(O.DATE,'DAY') AS DAYS,
ROUND(SUM(P.PRICE*OD.QUANTITY),2) AS REVENUE
FROM ORDERS O
JOIN ORDER_DETAILS OD
ON OD.ORDER_ID = O.ORDER_ID
JOIN PIZZAS P
ON P.PIZZA_ID = OD.PIZZA_ID
GROUP BY DAYS ;
--21. Rank Pizzas by Revenue.
SELECT PT.NAME , ROUND(SUM(P.PRICE*OD.QUANTITY),2) AS REVENUE,
RANK()OVER(ORDER BY SUM(P.PRICE*OD.QUANTITY) DESC ) AS RANKING
FROM PIZZAS P
JOIN PIZZA_TYPES PT
ON P.PIZZA_TYPE_ID =PT.PIZZA_TYPE_ID
JOIN ORDER_DETAILS OD
ON P.PIZZA_ID = OD.PIZZA_ID
GROUP BY PT.NAME;
--22. Rank Categories by Revenue.
SELECT PT.CATEGORY , ROUND(SUM(P.PRICE*OD.QUANTITY),2) AS REVENUE,
RANK()OVER(ORDER BY SUM(P.PRICE*OD.QUANTITY) DESC ) AS RANKING
FROM PIZZAS P
JOIN PIZZA_TYPES PT
ON P.PIZZA_TYPE_ID =PT.PIZZA_TYPE_ID
JOIN ORDER_DETAILS OD
ON P.PIZZA_ID = OD.PIZZA_ID
GROUP BY PT.CATEGORY;
--23.  Monthly Revenue Trend.
SELECT DATE_TRUNC('MONTH',O.DATE)AS MONTH,
ROUND(SUM(P.PRICE*OD.QUANTITY),2) AS REVENUE
FROM ORDERS O
JOIN ORDER_DETAILS OD
ON O.ORDER_ID = OD.ORDER_ID
JOIN PIZZAS P
ON P.PIZZA_ID = OD.PIZZA_ID
GROUP BY MONTH
ORDER BY MONTH;
--24. Least Selling Pizza.
SELECT PT.NAME, SUM(OD.QUANTITY) AS SOLD_PIZZA
FROM PIZZA_TYPES PT
JOIN PIZZAS P
ON P.PIZZA_TYPE_ID = PT.PIZZA_TYPE_ID
JOIN ORDER_DETAILS OD
ON P.PIZZA_ID = OD.PIZZA_ID
GROUP BY PT.NAME 
ORDER BY SOLD_PIZZA 
LIMIT 1;
--25. Create Monthly Sales View.
CREATE VIEW MONTHLY_SALES AS
SELECT DATE_TRUNC('MONTH',O.DATE)AS MONTH,
ROUND(SUM(P.PRICE*OD.QUANTITY),2) AS REVENUE
FROM ORDERS O
JOIN ORDER_DETAILS OD
ON O.ORDER_ID = OD.ORDER_ID
JOIN PIZZAS P
ON P.PIZZA_ID = OD.PIZZA_ID
GROUP BY MONTH
ORDER BY MONTH;
